// Initialize with real seed assignments (no mock boss/POI data)
function initializeSeedData() {
  const seedData = [];
  const nightlords = ['Gladius', 'Adel', 'Gnoster', 'Maris', 'Libra', 'Fulghor', 'Caligo', 'Heolstor'];
  const maps = ['Default', 'Mountaintop', 'Crater', 'Rotted Woods', 'Noklateo'];
  
  // Each nightlord has seeds in ranges: Gladius 0-39, Adel 40-79, etc.
  nightlords.forEach((nightlord, nightlordIndex) => {
    const baseNumber = nightlordIndex * 40;
    
    // Each nightlord has 20 default seeds + 5 seeds for each shifting earth
    for (let i = 0; i < 40; i++) {
      const seed = baseNumber + i;
      let mapType;
      
      if (i < 20) {
        mapType = 'Default';
      } else {
        // Distribute remaining 20 seeds across 4 shifting earth maps (5 each)
        const shiftingEarthIndex = Math.floor((i - 20) / 5);
        mapType = ['Mountaintop', 'Crater', 'Rotted Woods', 'Noklateo'][shiftingEarthIndex];
      }
      
      // Create row with basic info only - no mock data
      const row = new Array(50).fill('');
      row[0] = seed;
      row[1] = nightlord;
      row[2] = mapType;
      // All other fields empty - no mock boss/POI data
      
      seedData.push(row);
    }
  });
  
  return seedData;
}

